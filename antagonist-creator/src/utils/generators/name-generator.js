export const generateRandomCreatureName = () => {
  const syllableCount = Math.floor(Math.random() * 3) + 1;
  const result = [];

  if (syllableCount >= 1) {
    result.push(rand(creatureNameSyllables[0]));
  }
  if (syllableCount >= 2) {
    result.push(rand(creatureNameSyllables[1]));
  }
  if (syllableCount === 3) {
    result.push(rand(creatureNameSyllables[2]));
  }

  return result.join("").toLowerCase();
};
